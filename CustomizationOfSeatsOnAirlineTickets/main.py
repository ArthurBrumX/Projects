# Personalização de Assentos em Passagens Aéreas

# Em meio à ansiedade e antecipação de uma nova aventura, a compra de uma passagem de avião é o primeiro passo emocionante para desvendar 
# um novo destino.

# Parte 1: A Emoção da Decisão
# A decisão começa no momento em que você decide sua próxima aventura. Com destinos que variam de paisagens pitorescas 
# a metrópoles vibrantes, a escolha é infinita.

# Parte 2: Personalizando Sua Viagem
# A personalização é a palavra perfeita quando se trata da compra de passagens aéreas modernas e expetaculares.

# Parte 3: O Processo de Escolha do Assento
# O processo de escolha do assento é uma experiência interativa que faz parte da compra da passagem de avião, principalmente 
# quando conta com um mapa moderno interativo dos assentos livres e ocupados. 

# Parte 4: A Aventura Começa
# Após escolher seu assento e confirmar sua passagem, você está oficialmente pronto para embarcar em sua jornada.
# Para que a viajem aconteça sem nenhum problema no aeroporto, você deverá desenvolver um sistema que definirá quantas fileiras 
# tem no avião, e quantos assentos por fileira. Posteriormente você apresentará um menu de opções onde o usuário poderá reservar um 
# assento, mostrar os assentos reservados e sair do programa.

