# Análise de lucro de mercadorias

# O Sr. Ederson é um comerciante experiente que deseja otimizar seus negócios. Para isso, ele precisa de um programa 
# que o ajude a calcular e analisar o lucro de suas mercadorias. O Sr. Ederson decidiu pedir a ajuda de um 
# desenvolvedor de software para criar um algoritmo que o auxilie nessa tarefa.

# O algoritmo deve receber as informações de cada mercadoria.
# Após calcular o lucro de cada mercadoria, o algoritmo deve classifica-las em três categorias:

# a) Mercadorias com lucro menor que 10%
# b) Mercadorias com lucro entre 10% e 20%
# c) Mercadorias com lucro maior que 20%
# O algoritmo deve então contar quantas mercadorias se enquadram em cada categoria e exibir esses números.
# Além disso, o algoritmo deve calcular e exibir:
# ● O valor total de compra de todas as mercadorias.
# ● O valor total de venda de todas as mercadorias.
# ● O lucro total de todas as mercadorias.
# Exemplo de Entrada:
# Camiseta 20.00 25.00
# Tênis 50.00 80.00
# Relógio 100.00 120.00
# Boné 10.00 15.00
# Exemplo de Saída:
# Mercadorias com lucro menor que 10%: 0
# Mercadorias com lucro entre 10% e 20%: 1
# Mercadorias com lucro maior que 20%: 3
# Valor total de compra: 180.00
# Valor total de venda: 240.00
# Lucro total: 60.00