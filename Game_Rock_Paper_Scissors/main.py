# O Jogo Pedra, Papel e Tesoura: Uma Diversão Universal
# O jogo Pedra, Papel e Tesoura é uma das brincadeiras mais simples e universais já inventadas. 
# Seu apelo universal é evidenciado pelo fato de que é conhecido e jogado em praticamente todas as culturas 
# ao redor do mundo, embora com nomes e variações ligeiramente diferentes.
# As regras do jogo são tão descomplicadas quanto podem ser. Dois jogadores escolhem simultaneamente entre 
# três opções: pedra, papel ou tesoura. Cada opção vence outra e é derrotada por outra, formando uma hierarquia clara. 
# A pedra quebra a tesoura, a tesoura corta o papel, e o papel envolve a pedra. Se ambos os jogadores 
# escolherem a mesma opção, o resultado é um empate e geralmente se joga novamente.
# O que torna o jogo tão fascinante é a sua imprevisibilidade. Não há vantagem estratégica real, e a vitória muitas vezes se 
# resume à sorte. Essa simplicidade e aleatoriedade tornam o Pedra, Papel e Tesoura um jogo justo, acessível a 
# todas as idades e habilidades.
