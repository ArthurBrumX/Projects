# Campeonato de Futebol

# CAMPEONATO: Dois times, TIME A e TIME B, participam de um campeonato de futebol, juntamente com outros times. 
# Cada vitória conta três pontos, cada empate um ponto. Fica melhor classificado no campeonato um time que tenha mais pontos. 
# Em caso de empate no número de pontos, fica melhor classificado o time que tiver maior saldo de gols. 
# Se o número de pontos e o saldo de gols forem os mesmos para os dois times, então os dois times estão empatados no campeonato. 
# Dados os números de vitórias e empates, e os saldos de gols dos dois times, sua tarefa é determinar qual dos dois está melhor 
# classificado, ou se eles estão empatados no campeonato.

# DADOS DE ENTRADA:
# A entrada é composta por números inteiros, respectivamente o número de vitórias, empates e saldo de gols do TIME A, 
# o número de vitórias do TIME B, o número de empates do TIME B e o saldo de gols do TIME B.
 
# DADOS DE SAÍDA:
# De acordo com as informações apresentadas, seu programa em Python deve imprimir uma única linha. 
# Se TIME A é melhor classificado que TIME B, a linha deve conter apenas ‘TIME A’; se TIME B é melhor 
# classificado que TIME A, a linha deve conter apenas ‘TIME B’; e se os dois times estão empatados a linha deve conter 
# apenas o caractere ‘EMPATE’.