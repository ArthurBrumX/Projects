# Cálculo do Cabo de Estaiamento

# A torre estaiada é um tipo de estrutura formada pela união de módulos sobrepostos, formando uma torre que deve ser  
# empilhada  com cabos de aço de mesma largura desde a base até seu topo, e que não permite sustentação própria 
# contra os efeitos do vento, por isso deve ser bem presa ao solo para impedir que venha a cair.
# Utilizando esse modelo, a empresa Estaio Torres precisa desenvolver um software para calcular o tamanho do cabo de Estaio 
# de aço que fará a sustentação da torre. Você deverá desenvolver um software que, baseado na altura da torre (h) e  na 
# distância entre a torre e ponto fixo do cabo de estaio (d), resulte na quantidade em metros do cabo (x).

 
# Ex:
# Informe a altura do ponto da torre = 8
# Informe a distância entre a torre e o ponto de estaio, onde o cabo de aço é fixo no chão = 6
# A quantidade de cabos é de = 10.00  metros